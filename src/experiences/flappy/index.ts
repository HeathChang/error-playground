/**
 * flappy — 탭 날갯짓 게임 (게임 카트리지).
 * 루프·입력·이벤트는 공통 콘솔(gameMachine)이, 캔버스 배관은 canvas.ts가 담당.
 */
import type { Experience } from '../../core/types';
import { canvasSupported, makeSeed, mountCanvas, type CanvasTheme } from '../canvas';
import { gameMachine, type Game, type GameEnv } from '../machine';
import { FlappyGame } from './game';
import { render } from './render';

function pickTheme(theme: 'light' | 'dark'): CanvasTheme {
  return theme === 'dark'
    ? { bg: '#0f172a', fg: '#f8fafc', accent: '#22c55e', muted: '#334155' }
    : { bg: '#e0f2fe', fg: '#0f172a', accent: '#22c55e', muted: '#94a3b8' };
}

function createFlappy(env: GameEnv): Game {
  const { canvas, ctx } = mountCanvas(env);
  const game = new FlappyGame({ width: env.width, height: env.height, seed: makeSeed(0x27d4eb2f) });
  const theme = pickTheme(env.theme);

  return {
    get status() {
      return game.status;
    },
    get score() {
      return game.score;
    },
    input() {
      game.input();
    },
    step(dt) {
      game.step(dt);
    },
    render() {
      if (ctx) render(ctx, game, theme);
    },
    destroy() {
      canvas.remove();
    },
  };
}

/** 코어 로더가 호출하는 팩토리. */
export function create(): Experience {
  return gameMachine(createFlappy, canvasSupported);
}
